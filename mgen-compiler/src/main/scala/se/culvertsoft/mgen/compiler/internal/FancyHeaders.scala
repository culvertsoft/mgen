package se.culvertsoft.mgen.compiler.internal

object FancyHeaders {

  def fileHeader(version: String) = s"""/********************************************************************************************************************
 ********************************************************************************************************************
 **********************************************************************************************************	
           *****                                                                                      *****
           *****               GENERATED WITH MGEN (${version}) ${nSpaces(48 - version.size)}*****
           *****                                                                                      *****		
 ********************************************************************************************************************
 ********************************************************************************************************************/"""

  val serializationSectionHeader = """
							
/********************************************************************************************************************
 ********************************************************************************************************************
 **********************************************************************************************************	
           *****                                                                                      *****
           *****                    TYPE METADATA ACCESS AND SERIALIZATION METHODS                    *****
           *****          (accessed primarily by (de-)serializers and for ORM functionality)          *****	
           *****                                                                                      *****		
 ********************************************************************************************************************
 ********************************************************************************************************************/	 		  
		  """

  val metadataSectionHeader = """
							
/********************************************************************************************************************
 ********************************************************************************************************************
 **********************************************************************************************************	
           *****                                                                                      *****
           *****                                    TYPE METADATA                                     *****
           *****             (generally speaking, it's a bad idea to edit this manually)              *****	
           *****                                                                                      *****		
 ********************************************************************************************************************
 ********************************************************************************************************************/	 		  
		  """

  private def nChars(n: Int, c: Character): String = {
    val sb = new StringBuilder
    for (i <- 0 until n)
      sb.append(c)
    sb.toString
  }
  private def nStars(n: Int): String = {
    nChars(n, '*')
  }

  private def nSpaces(n: Int): String = {
    nChars(n, ' ')
  }

}